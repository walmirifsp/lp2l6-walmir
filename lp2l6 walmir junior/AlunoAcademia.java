public class AlunoAcademia {
		private String nome;
		private int idade;
		private float peso;
		private float altura;
		private String objetivo;
		
		public AlunoAcademia(String nome, int idade, float peso, float altura, String objetivo){
			this.nome = nome;
			this.idade = idade;
			this.peso = peso;
			this.altura = altura;
			this.objetivo = objetivo;
		}
}